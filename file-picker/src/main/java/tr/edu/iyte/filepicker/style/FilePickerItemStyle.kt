 * @property textColor Color of the list item texts
 * @property secondaryTextColor Color of the secondary texts of list items
 */
class FilePickerItemStyle(val backgroundColor: Int,
                          val backgroundRippleColor: Int,
                          val drawableTintColor: Int,
                          val textColor: Int,
                          val secondaryTextColor: Int)